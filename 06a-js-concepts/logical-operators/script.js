// AND
const conditionA = true;
const conditionB = false;

if (conditionA && conditionB) {
    console.log(`AND ${conditionA} && ${conditionB} = Condição atingida`);
} else {
    console.log(`AND ${conditionA} && ${conditionB} = Condição não atingida`);
}

// OR
const conditionC = true;
const conditionD = false;

if (conditionC || conditionD) {
    console.log(`OR ${conditionC} || ${conditionD} = Condição atingida`);
} else {
    console.log(`OR ${conditionC} || ${conditionD} = Condição não atingida`);
}